package JavaProgram;

public class ArithmeticOp {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;
        int c = 30;

        System.out.println("a+(b*c)=" +(a+(b*c)));
        System.out.println("c+(a/b)=" +(c+(a/b)));
        System.out.println("(a%b)+c=" +((a%b)+c));
        System.out.println("(a*b)+c=" +((a*b)+c));
        System.out.println("a<b=" +(a<b));
        System.out.println("b<c=" +(b<c));
        System.out.println("a>b=" +(a>b));
        System.out.println("b>c=" +(b>c));



    }
}
